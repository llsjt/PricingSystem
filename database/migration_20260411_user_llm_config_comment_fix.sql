  MODIFY COLUMN user_id BIGINT NOT NULL COMMENT '用户ID',
  MODIFY COLUMN token_hash CHAR(64) NOT NULL COMMENT '刷新令牌哈希',
  MODIFY COLUMN expires_at DATETIME NOT NULL COMMENT '过期时间',
  MODIFY COLUMN revoked_at DATETIME NULL COMMENT '撤销时间',
  MODIFY COLUMN last_used_at DATETIME NULL COMMENT '最后使用时间',
  MODIFY COLUMN ip_address VARCHAR(64) NULL COMMENT '客户端IP',
  MODIFY COLUMN user_agent VARCHAR(255) NULL COMMENT '客户端标识',
  MODIFY COLUMN created_at DATETIME NOT NULL DEFAULT CURRENT_TIMESTAMP COMMENT '创建时间',
  MODIFY COLUMN updated_at DATETIME NOT NULL DEFAULT CURRENT_TIMESTAMP ON UPDATE CURRENT_TIMESTAMP COMMENT '更新时间',
  COMMENT = '刷新令牌会话表';

