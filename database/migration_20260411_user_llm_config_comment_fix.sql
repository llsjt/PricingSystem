ALTER TABLE schema_migration_history
  MODIFY COLUMN version VARCHAR(128) NOT NULL COMMENT '迁移版本',
  MODIFY COLUMN checksum CHAR(64) NOT NULL COMMENT '校验值（SHA256）',
  MODIFY COLUMN description VARCHAR(255) NOT NULL COMMENT '迁移说明',
  MODIFY COLUMN applied_at DATETIME NOT NULL DEFAULT CURRENT_TIMESTAMP COMMENT '应用时间',
  COMMENT = '数据库迁移历史表';

ALTER TABLE sys_user
  MODIFY COLUMN role VARCHAR(20) NOT NULL DEFAULT 'USER' COMMENT '用户角色',
  MODIFY COLUMN token_version INT NOT NULL DEFAULT 0 COMMENT '令牌版本号';

ALTER TABLE pricing_task
  MODIFY COLUMN task_status VARCHAR(20) NOT NULL DEFAULT 'QUEUED' COMMENT '任务状态',
  MODIFY COLUMN requested_by_user_id BIGINT NULL COMMENT '发起用户ID',
  MODIFY COLUMN trace_id VARCHAR(64) NULL COMMENT '链路追踪ID',
  MODIFY COLUMN idempotency_key VARCHAR(128) NULL COMMENT '幂等键',
  MODIFY COLUMN retry_count INT NOT NULL DEFAULT 0 COMMENT '重试次数',
  MODIFY COLUMN failure_reason VARCHAR(255) NULL COMMENT '失败原因',
  MODIFY COLUMN started_at DATETIME NULL COMMENT '开始时间',
  MODIFY COLUMN completed_at DATETIME NULL COMMENT '完成时间',
  MODIFY COLUMN strategy_goal VARCHAR(50) NOT NULL DEFAULT 'MAX_PROFIT' COMMENT '策略目标',
  MODIFY COLUMN constraint_text VARCHAR(1000) NULL COMMENT '约束条件文本',
  MODIFY COLUMN llm_api_key_enc VARCHAR(512) DEFAULT NULL COMMENT 'Fernet 加密的用户 API Key（重试用）',
  MODIFY COLUMN llm_base_url VARCHAR(255) DEFAULT NULL COMMENT '用户 LLM Base URL',
  MODIFY COLUMN llm_model VARCHAR(100) DEFAULT NULL COMMENT '用户 LLM 模型名称';

ALTER TABLE pricing_result
  MODIFY COLUMN review_required TINYINT NOT NULL DEFAULT 1 COMMENT '是否需要人工审核',
